function Get-IntuneRBACRole
{
    <#
    .SYNOPSIS
    This function is used to get Intune RBAC Role Definitions from the Graph API REST interface

        DeviceManagementRBAC.ReadWrite.All
        https://graph.microsoft.com/beta/deviceManagement/roleDefinitions?$filter=displayName eq 'PAW-CSM-Intune-Operator'
        https://graph.microsoft.com/beta/deviceManagement/roleDefinitions?$filter=displayName eq 'EUD-Intune-Operator'

    .DESCRIPTION
    The function connects to the Graph API Interface and gets any RBAC Role Definitions
    .EXAMPLE
    Get-RBACRole
    Returns any RBAC Role Definitions configured in Intune
    #>
    [cmdletbinding(DefaultParameterSetName='All')]
    param (
        [Parameter(ParameterSetName='DisplayName')]
        [string] $DisplayName,

        [Parameter(Mandatory,ParameterSetName="ID")]
        [guid] $ID
    )

    $apiVersion = 'beta'
    $resource = 'deviceManagement/roleDefinitions'

    switch ($PSCmdlet.ParameterSetName)
    {
        'DisplayName'
        {
            $resource = $resource + "?`$filter=displayName eq '$DisplayName'"
            break
        }
        'ID'
        {
            $resource = $resource + '/' + $ID
            break
        }
    }

    try
    {
        $uri = "https://graph.microsoft.com/$apiVersion/$resource"
        $return = Invoke-MgGraphRequest -Method Get -Uri $uri
    } catch
    {
        New-Exception -Exception $_.Exception
    }

    if ($PSCmdlet.ParameterSetName -eq 'DisplayName')
    {
        $return.value
    } else
    {
        $return
    }
}
