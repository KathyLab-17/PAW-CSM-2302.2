function Set-ProactiveRemediationScriptAssignment {
    <#
    .SYNOPSIS
    This function is used to add a proactive remediation script assignment using the Graph API REST interface
    .DESCRIPTION
    This function is used to add a proactive remediation script assignment using the Graph API REST interface
    .EXAMPLE
    Set-ProactiveRemediationScriptAssignment -RemediationScriptId $RemediationScriptId
    #>

    [cmdletbinding()]
    param(
        [Parameter(Mandatory)]
        [string] $RemediationScriptId,

        [Parameter(Mandatory)]
        [string] $aadGroupId,

        [Parameter(Mandatory)]
        [bool] $runRemediationScript,

        [Parameter(Mandatory)]
        [int] $interval,

        [Parameter(Mandatory)]
        [bool] $useUtc,

        [Parameter(Mandatory)]
        [string] $time
    )

    $apiVersion = 'beta'
    $resource = "deviceManagement/deviceHealthScripts/$RemediationScriptId/assign"

    $aadDisplayName = ConvertTo-AADDisplayName -Id $aadGroupId
    Write-Log -Message "      Adding: $aadDisplayName" -WriteHost Green
    $aadGroupObjectId = (Get-AADGroup -DisplayName $aadDisplayName).id

$body = @"
{
"deviceHealthScriptAssignments": [
    {
      "@odata.type": "#microsoft.graph.deviceHealthScriptAssignment",
        "target": {
          "@odata.type": "#microsoft.graph.groupAssignmentTarget",
          "deviceAndAppManagementAssignmentFilterId": null,
          "deviceAndAppManagementAssignmentFilterType": "none",
          "groupId": "$aadGroupObjectId"
        },
        "runRemediationScript": "$runRemediationScript",
        "runSchedule": {
          "@odata.type": "microsoft.graph.deviceHealthScriptDailySchedule",
          "interval": $interval,
          "useUtc": "$useUtc",
          "time": "$time"
        }
    }
  ]
}
"@

try {
        $uri = "https://graph.microsoft.com/$apiVersion/$resource"
        Invoke-MgGraphRequest -Method Post -Uri $uri -Body $body -ContentType 'application/json'
    }
    catch {
        New-Exception -Exception $_.Exception
    }
}
