function Remove-DeviceConfigurationList {
    <#
    .SYNOPSIS
    Removes a list of Device Configuration policies
    .PARAMETER JSONFileList
    The list of json files that contain the Device Configuration policies to remove
    .EXAMPLE
    $jsonFileList = Get-ChildItems -Path .\JSON\DeviceConfiguration\ | Select Fullname -Expand FullName
    Remove-DeviceConfigurationList -JSONFileList $jsonFileList

    In this example, the list of json files is created with the Get-ChildItem cmdlet and the
    results are passed directly to the Remove-DeviceConfigurationList for processing
    #>
    [cmdletbinding()]
    param (
        [Parameter(Mandatory)]
        [string[]] $JSONFileList
    )

    Write-Host "`nDevice Configuration Policy:"

    foreach ($JSONFile in $JSONFileList) {

        $DeviceConfigurationPolicyJSON = Get-JSONContent -JSONFile $JSONFile -ExcludeProperty 'id','createdDateTime','lastModifiedDateTime','priority'

        if (Test-DeviceConfigurationPolicy -DisplayName $DeviceConfigurationPolicyJSON.displayName) {
            Write-Host '  Removing:' $DeviceConfigurationPolicyJSON.DisplayName -f Red

            $deviceConfiguration = Get-DeviceConfigurationPolicy -DisplayName $DeviceConfigurationPolicyJSON.displayName

            $apiVersion = 'beta'
            $resource = "deviceManagement/deviceConfigurations/$($deviceConfiguration.Id)"

            try {
                $uri = "https://graph.microsoft.com/$apiVersion/$resource"
                Invoke-MgGraphRequest -Method Delete -Uri $uri
            }
            catch {
                New-Exception -Exception $_.Exception
            }
        }
        else {
            Write-Host '  Not Found:' $DeviceConfigurationPolicyJSON.DisplayName -ForegroundColor Yellow
        }
    }
}
