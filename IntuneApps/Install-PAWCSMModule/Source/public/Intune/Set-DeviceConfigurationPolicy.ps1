function Set-DeviceConfigurationPolicy {
    <#
    .SYNOPSIS
    This function is used to add an device configuration policy using the Graph API REST interface
    .DESCRIPTION
    The function connects to the Graph API Interface and adds a device configuration policy
    .EXAMPLE
    Set-DeviceConfigurationPolicy -JSON $JSON
    Adds a device configuration policy in Intune
    #>
    [cmdletbinding()]
    param (
        [parameter(Mandatory)]
        [psobject] $JSON
    )

    # Assigments are handled in a seperate part of the import
    $body = $JSON |
        Select-Object -Property * -ExcludeProperty assignments |
            ConvertTo-Json -Depth 5

    $apiVersion = 'beta'
    $resource = 'deviceManagement/deviceConfigurations'

    Write-Host '  Creating:' $JSON.DisplayName -ForegroundColor Green

    try {
        $uri = "https://graph.microsoft.com/$apiVersion/$resource"
        Invoke-MgGraphRequest -Method Post -Uri $uri -Body $body -ContentType 'application/json'
    }
    catch {
        New-Exception -Exception $_.Exception
    }
}
