function Export-DeviceSettingsCatalog {
    <#
    .SYNOPSIS
    This function is used to list and export all device settings catalog from the Graph API REST interface

    .DESCRIPTION
    The function connects to the Graph API Interface and gets any device compliance policies
    
    .PARAMETER OutputFolderPath
    The full Output folder path, all JSON files will be created here, if folder does not exist is created
    
    .EXAMPLE
    Export-DeviceSettingsCatalogList -OutputFolderPath "C:\Users\john\Desktop\Intune-JSON"
    Create a JSON file per each existent Device Compliance Policy on the defined Output Folder
    
    .LINK
    https://docs.microsoft.com/en-us/graph/permissions-reference#intune-device-management-permissions
    
    .NOTES
    Requires scope:
        DeviceManagementConfiguration.Read.All,
        DeviceManagementServiceConfig.ReadWrite.All
    #>

    param (
        [parameter(Mandatory=$true)]
        [string] $OutputFolderPath
    )

    $apiVersion = 'beta'
    $resource = "deviceManagement/configurationPolicies?`$filter=technologies has 'mdm'"

    $type = 'DeviceSettingsCatalog'
    $exportPath = "$($OutputFolderPath)\$($type)"

    try {
        if(!(Test-Path $exportPath)){
    
            Write-Log -Message "`n  Export directory $($exportPath) doesn't exists and will be created." -WriteHost Yellow
            $null = New-Item -ItemType Directory -Path $exportPath
        }
        elseif(Test-Path $exportPath){
            Write-Log -Message "`n  Export directory $($exportPath) exists and will be used." -WriteHost Green
        }

        $uri = "https://graph.microsoft.com/$apiVersion/$($resource)&`$expand=settings,assignments"
        $result = (Invoke-MgGraphRequest -Method Get -Uri $uri)

        $policyList = @()
        $policyList += $result.value

        # Retrieving settings of all Device Settings Catalog policies, via the nextLink value
        While ($result.'@odata.nextLink') {
            $result = Invoke-MgGraphRequest -Method Get -Uri $result.'@odata.nextLink'
            $policyList += $result.value    
        }

        foreach($policy in $policyList){

            Write-Log -Message "`n  Processing settings catalog policy: $($policy.name)" -WriteHost Green

            $exportFileName = "$($policy.name).json"

            # roleScopeTagIds resolution and matching 
            Write-Log -Message "  Converting Role Scope Tag to internal tag name" -WriteHost White
            $policy.roleScopeTagIds  = ConvertTo-TagInternalName -policy $policy

            # Group assignment resolution and matching 
            foreach($assignment in $policy.assignments.target){
                Write-Log -Message "  Converting Azure AD Group ObjectID to internal group name" -WriteHost White
                $groupObjectId = $Policy.assignments.target.groupId
            
                if ($groupId = ConvertTo-AADGroupInternalName -groupObjectId $groupObjectId){
                    $assignment.groupId = $groupId
                }
            } 
            
            # Export modified/sanatized config file
            $policy | ConvertTo-Json -Depth 100 | Out-File "$($exportPath)\$($exportFileName)"

            Write-Log -Message "  Policy $($policy.name) exported successfully." -WriteHost Green
        }

    }
    catch {
        New-Exception -Exception $_.Exception
    }
}
