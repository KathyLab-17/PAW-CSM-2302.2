function Set-NamedLocation {
    <#
    .SYNOPSIS
    Creates a new Conditional Access Named Location in AAD
    .PARAMETER NamedLocationObject
    The Named Location object imported from the JSON configuration
    .LINK
    https://docs.microsoft.com/en-us/powershell/module/microsoft.graph.identity.signins/new-mgidentityconditionalaccessnamedlocation?view=graph-powershell-1.0
    .LINK
    https://docs.microsoft.com/en-us/graph/api/conditionalaccessroot-post-namedlocations?view=graph-rest-1.0&tabs=http
    .NOTES
    Requires scope:
        Policy.Read.All
        Policy.ReadWrite.ConditionalAccess
    #>
    [cmdletbinding()]
    param(
        [Parameter(Mandatory)]
        [psobject] $NamedLocationObject
    )

    Write-Host '    Creating:' $NamedLocationObject.DisplayName -ForegroundColor Green

    try {
        $bodyParameter = ConvertTo-Hashtable -PsObject $NamedLocationObject
        New-MgIdentityConditionalAccessNamedLocation -BodyParameter $bodyParameter
    }
    catch {
        New-Exception -Exception $_.Exception
    }
}
