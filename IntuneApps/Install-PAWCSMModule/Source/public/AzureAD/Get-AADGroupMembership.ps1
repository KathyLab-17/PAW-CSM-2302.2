function Get-AADGroupMembership {
    <#
    .SYNOPSIS
    Returns the membership list of an AAD group by Name or Id
    #>
    [CmdletBinding()]
    param (
        [Parameter(Mandatory, ParameterSetName='DisplayName')]
        [string] $DisplayName,

        [Parameter(Mandatory, ParameterSetName='ID')]
        [guid] $ID
    )

    if ($DisplayName) {
        $ID = (Get-MgGroup -Filter "DisplayName eq '$DisplayName'").Id
    }

    Get-MgGroupMember -GroupId $ID
}
