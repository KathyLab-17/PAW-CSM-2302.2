function Get-AADUser {
    <#
    .SYNOPSIS
    Gets an AAD User by either it's display name or the internal id
    .PARAMETER DisplayName
    The Users AAD Display Name
    .PARAMETER PAWNameId
    The internal module user id
    .Example
    Get-AADUser -DisplayName 'CSM Azure Break Glass 1'
    In this example the PCSM Azure Break Glass 1 useer object is returned
    .Example
    Get-AADUser -PAWNameId user-azure-break-glass-1
    In this example, user-azure-break-glass-1 is searched for in the user list.
    The CSM Azure Break Glass 1 user Display Name is returned and AAD is searched with it.
    #>
    [CmdletBinding()]
    param (
        [Parameter(Mandatory, ParameterSetName='DisplayName')]
        [string] $DisplayName,

        [Parameter(Mandatory, ParameterSetName='InternalName')]
        [string] $PAWNameId
    )

    if ($PAWNameId) {
        $DisplayName = ConvertTo-AADDisplayName -Id $DeviceAdminGroupId
    }

    Get-MgUser -Filter "DisplayName eq '$DisplayName'"
}
