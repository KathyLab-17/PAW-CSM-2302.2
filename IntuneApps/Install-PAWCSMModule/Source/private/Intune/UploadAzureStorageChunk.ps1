function UploadAzureStorageChunk($sasUri, $id, $body) {
    <#
    .SYNOPSIS
    This function is used to upload files to Azure Storage in small chunks
    .DESCRIPTION
    This function is used to upload files to Azure Storage in small chunks
    .PARAMETER sasUri
   
    .PARAMETER id

    .PARAMETER body
    #>

    $uri = "$sasUri&comp=block&blockid=$id";
    $request = "PUT $uri";

    $iso = [System.Text.Encoding]::GetEncoding("iso-8859-1");
    $encodedBody = $iso.GetString($body);
    $headers = @{
        "x-ms-blob-type" = "BlockBlob"
    };

    Write-Log $request

    try {
        $response = Invoke-WebRequest -Method Put -Uri $uri -Headers $headers -Body $encodedBody -UseBasicParsing
    }
    catch {
        Write-Host -ForegroundColor Red $request;
        Write-Host -ForegroundColor Red $_.Exception.Message;
        throw;
    }
}
