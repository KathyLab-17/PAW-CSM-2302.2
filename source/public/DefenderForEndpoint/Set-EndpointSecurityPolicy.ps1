function Set-EndpointSecurityPolicy {
    <#
    .SYNOPSIS
    This function is used to add an Endpoint Security policy using the Graph API REST interface
    .DESCRIPTION
    The function connects to the Graph API Interface and adds an Endpoint Security  policy
    .EXAMPLE
    Add-EndpointSecurityDiskEncryptionPolicy -JSON $JSON
    Adds an Endpoint Security Policy in Endpoint Manager
    #>
    [cmdletbinding()]
    param (
        [parameter(Mandatory)]
        [guid] $TemplateId,

        [parameter(Mandatory)]
        [psobject] $JSON
    )

    $apiVersion = 'beta'
    $resource = "deviceManagement/templates/$TemplateId/createInstance"

    $body = $JSON | ConvertTo-Json -Depth 5

    try {
        $uri = "https://graph.microsoft.com/$apiVersion/$resource"
        Invoke-MgGraphRequest -Method Post -Uri $uri -Body $body -ContentType 'application/json'
    }
    catch {
        New-Exception -Exception $_.Exception
    }
}
