function Get-IntuneApplication {
    <#
    .SYNOPSIS
    This function is used to get applications from the Graph API REST interface
    .DESCRIPTION
    The function connects to the Graph API Interface and gets any applications added
    .PARAMETER DisplayName
    The Display Name of the app to search for
    .PARAMETER ID
    The Application ID to search for
    .EXAMPLE
    Get-IntuneApplication
    Returns any applications configured in Intune
    #>
    [cmdletbinding(DefaultParameterSetName='All')]
    param (
        [Parameter(ParameterSetName='DisplayName')]
        [string] $DisplayName,

        [Parameter(Mandatory,ParameterSetName="ID")]
        [guid] $ID,

        [Parameter(ParameterSetName="DisplayName")]
        [string] $AppType
    )

    $apiVersion = 'beta'
    $resource = 'deviceAppManagement/mobileApps'

    switch ($PSCmdlet.ParameterSetName)
    {
        'DisplayName' {
            $DisplayName = [uri]::EscapeDataString($DisplayName)
            
            if ($AppType -eq "WinGet") {
                $resource = $resource + "?`$filter=(displayName eq '$DisplayName') and (isof('microsoft.graph.winGetApp'))"
            }
            else {
                $resource = $resource + "?`$filter=displayName eq '$DisplayName'"
            }

            break
        }
        'ID' {
            $resource = $resource + '/' + $ID
            break
        }
    }

    try {
        $uri = "https://graph.microsoft.com/$apiVersion/$resource"
        $return = Invoke-MgGraphRequest -Method Get -Uri $uri
    }
    catch {
        New-Exception -Exception $_.Exception
    }

    if ($PSCmdlet.ParameterSetName -eq 'DisplayName') {
        $return.value
    }
    else {
        $return
    }
}
