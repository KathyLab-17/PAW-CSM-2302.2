function Get-IntuneRoleScopeTag {
    <#
    .SYNOPSIS
    This function is used to add an RBAC Scope Tag from the Graph API REST interface
    .DESCRIPTION
    The function connects to the Graph API Interface and adds an RBAC Scope Tag
    .EXAMPLE
    Set-IntuneRoleScopeTag -JSON $JSON
    #>
    [cmdletbinding()]
    param (
        [Parameter(Mandatory)]
        [string] $DisplayName
    )

    $apiVersion = 'beta'
    $resource = 'deviceManagement/roleScopeTags'

    try {
        $uri = "https://graph.microsoft.com/$apiVersion/$resource"
        ( Invoke-MgGraphRequest -Method Get -Uri $uri ).Value | Where-Object { ($_.'displayName').contains("$DisplayName") }
    }
    catch {
        New-Exception -Exception $_.Exception
    }
}
