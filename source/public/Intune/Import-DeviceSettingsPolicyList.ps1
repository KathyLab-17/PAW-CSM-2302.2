function Import-DeviceConfigurationADMXList {
    <#
    .SYNOPSIS
    Imports a list of Device Configuration ADMX policies
    .PARAMETER JSONFileList
    The list of json files that contain the Device Configuration ADMX policies to import
    .EXAMPLE
    $jsonFileList = Get-ChildItems -Path .\JSON\DeviceConfigurationADMX\ | Select Fullname -Expand FullName
    Import-DeviceConfigurationADMXList -JSONFileList $jsonFileList

    In this example, the list of json files is created with the Get-ChildItem cmdlet and the
    results are passed directly to the Import-DeviceConfigurationADMXList for processing
    #>
    [cmdletbinding()]
    param (
        [Parameter(Mandatory)]
        [string[]] $JSONFileList
    )

	Write-Log -Message "`nDevice Configuration Group Policy:" -WriteHost White
	foreach ($JSONFile in $JSONFileList) {

		$groupPolicyConfigurationJSON = Get-JSONContent -JSONFile $JSONFile

		if (Test-DeviceConfigurationADMXPolicy -DisplayName $groupPolicyConfigurationJSON.DisplayName) {
			Write-Log -Message "  Existing: $($groupPolicyConfigurationJSON.DisplayName)" -WriteHost Yellow
		}
		else {
			Write-Log -Message "  Creating: $($groupPolicyConfigurationJSON.DisplayName)" -WriteHost Green

			$GroupPolicyConfiguration = Set-DeviceConfigurationADMXPolicy -DisplayName $groupPolicyConfigurationJSON.DisplayName

			foreach ($definition in $groupPolicyConfigurationJSON.Definitions) {

				$null = $definition.'definition@odata.bind' -match '\w{8}-\w{4}-\w{4}-\w{4}-\w{12}'
				$groupPolicyConfigurationDefinition = Get-GroupPolicyConfigurationDefinition -ID $matches[0]

				Write-Log -Message "    Setting: $($groupPolicyConfigurationDefinition.DisplayName) ($($matches[0]))" -WriteHost Green
				$policyDefinition = Set-GroupPolicyConfigurationDefinition -JSON $definition -ConfigurationID $GroupPolicyConfiguration.id
			}

			$aadDisplayName = ConvertTo-AADDisplayName -Id 'group-paw-global-devices'
			$TargetGroup = Get-AADGroup -DisplayName $aadDisplayName
			Write-Log -Message "  Assigning: $aadDisplayName" -WriteHost Green
			$assignment = Set-GroupPolicyConfigurationPolicyAssignment -ConfigurationId $GroupPolicyConfiguration.id -TargetGroupId $TargetGroup.Id -Assignment "groupAssignmentTarget"
		}
	}
}
