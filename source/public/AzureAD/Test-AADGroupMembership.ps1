function Test-AADGroupMembership {
    <#
    .SYNOPSIS
    A simple function that looks at the group membership of an AAD group and returns
    $true if the member is found $false if not found
    .NOTES
    Long term this will do a deep comparison of the template and what is deployed
    in the customer tenant
    #>
    param(
        [Parameter(Mandatory)]
        [guid] $AADGroupID,

        [Parameter(Mandatory)]
        [guid] $AADGroupMemberID
    )

    $groupMember = Get-AADGroupMembership -ID $AADGroupID

    if ($groupMember.Id -contains $AADGroupMemberID) {
        return $true
    }
    return $false
}
