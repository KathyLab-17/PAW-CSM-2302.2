function Export-NamedLocationList {
    <#
    .SYNOPSIS
    This function is used to list and export all Conditional Access Named Locations from the Graph API REST interface
    .DESCRIPTION
    The function connects to the Graph API Interface and gets any Conditional Access Named Locations
    .PARAMETER OutputFolderPath
    The full Output folder path, all JSON files will be created here, if folder does not exist is created
    .EXAMPLE
    Export-NamedLocationList -OutputFolderPath "C:\Users\john\Desktop\Intune-JSON"
    Create a JSON file per each existent Named Location on the defined Output Folder
    .LINK
    https://learn.microsoft.com/en-us/graph/permissions-reference#policy-permissions
    .NOTES
    Requires scope:
        Policy.Read.All
    #>

    param (
        [string] $OutputFolderPath
    )


  $apiVersion = "beta"
  $Resource = "identity/conditionalAccess/namedLocations"
  $type = 'NamedLocations'
  $exportPath = "$($OutputFolderPath)\$($type)"

    try {
        if(!(Test-Path $exportPath)){
            Write-Log -Message "Export directory $($exportPath) doesn't exists and will be created." -WriteHost Yellow
            $null = New-Item -ItemType Directory -Path $exportPath
        }
        elseif(Test-Path $exportPath){
            Write-Log -Message "Export directory $($exportPath) exists and will be used." -WriteHost Green
        }
        
        $uri = "https://graph.microsoft.com/$apiVersion/$($Resource)?`$filter=startswith(displayName,'PAW')"
        $NamedLocationList = (Invoke-MgGraphRequest -Method Get -Uri $uri).value
        foreach($NamedLocation in $NamedLocationList){
            Write-Log -Message "`n  Processing $($NamedLocation.displayName) " -WriteHost Green
            $exportFileName = "$($NamedLocation.displayName).json"
            $NamedLocation | ConvertTo-Json -Depth 100 | Out-File "$($exportPath)\$($exportFileName)"
            Write-Log -Message "Export of $($NamedLocation.displayName) completed! " -WriteHost Green
        }

    }
    catch {
       New-Exception -Exception $_.Exception
    }
}
