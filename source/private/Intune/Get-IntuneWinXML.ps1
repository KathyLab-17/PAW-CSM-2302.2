function Get-IntuneWinXML {
    <#
    .SYNOPSIS
    This function is used to provide the required input to create a Intune Detection Rule for a given application. Return values (XML) include:
        * ApplicationInfo.Name
        * ApplicationInfo.FileName
        * ApplicationInfo.SetupFile
        * ApplicationInfo.MsiInfo.MsiExecutionContext
        * ApplicationInfo.MsiInfo.MsiProductCode
        * ApplicationInfo.MsiInfo.ProductVersion
        * ApplicationInfo.MsiInfo.MsiPublisher
        * ApplicationInfo.MsiInfo.MsiRequiresReboot
        * ApplicationInfo.MsiInfo.MsiUpgradeCode

    .DESCRIPTION
    This function is used to provide the required input to create a Intune Detection Rule for a given application. Return values (XML) include:
        * ApplicationInfo.Name
        * ApplicationInfo.FileName
        * ApplicationInfo.SetupFile
        * ApplicationInfo.MsiInfo.MsiExecutionContext
        * ApplicationInfo.MsiInfo.MsiProductCode
        * ApplicationInfo.MsiInfo.ProductVersion
        * ApplicationInfo.MsiInfo.MsiPublisher
        * ApplicationInfo.MsiInfo.MsiRequiresReboot
        * ApplicationInfo.MsiInfo.MsiUpgradeCode
        
    .PARAMETER SourceFile

    .PARAMETER FileName

    .PARAMETER removeitem
    
    #> 

    param (
        [Parameter(Mandatory = $true)]
        $SourceFile,

        [Parameter(Mandatory = $true)]
        $fileName,

        [Parameter(Mandatory = $false)]
        [ValidateSet("false", "true")]
        [string]$removeitem = "true"
    )

    Test-SourceFile "$SourceFile"

    $Directory = [System.IO.Path]::GetDirectoryName("$SourceFile")

    Add-Type -Assembly System.IO.Compression.FileSystem
    $zip = [IO.Compression.ZipFile]::OpenRead("$SourceFile")

    $zip.Entries | where { $_.Name -like "$filename" } | foreach {

        [System.IO.Compression.ZipFileExtensions]::ExtractToFile($_, "$Directory\$filename", $true)

    }

    $zip.Dispose()

    [xml]$IntuneWinXML = gc "$Directory\$filename"

    return $IntuneWinXML

    if ($removeitem -eq "true") { remove-item "$Directory\$filename" }

}
