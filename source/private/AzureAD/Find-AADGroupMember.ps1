function Find-AADGroupMember {

    [cmdletbinding()]
    param(
        [Parameter(Mandatory)]
        [string] $GroupName,

        [Parameter()]
        [string] $UserJSONPath = (Resolve-Path -Path "$PSScriptRoot\..\..\JSON\Users\pawcsm.users.json"),

        [Parameter()]
        [string] $GroupJSONPath = (Resolve-Path -Path "$PSScriptRoot\..\..\JSON\Groups\pawcsm.groups.json")
    )

    $userList = Get-Content -Path $UserJSONPath | ConvertFrom-Json
    $groupList = Get-Content -Path $GroupJSONPath | ConvertFrom-Json

    $group = $groupList.Where({$_.DisplayName -eq $GroupName })

    if ($null -ne $group.MemberRef) {
        foreach ($member in $group.MemberRef ) {

            if ($member -match '^group*') {
                $groupList.Where({$_.Id -eq $member })
            }
            else {
                $userList.Where({$_.Id -eq $member })
            }
        }
    }
}
