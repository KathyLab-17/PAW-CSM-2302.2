function Get-AADObjectToSid {
    <#
    .SYNOPSIS
    Convert an Azure AD Object ID to SID
    .DESCRIPTION
    Converts an Azure AD Object ID to a SID.
    Author: Oliver Kieselbach (oliverkieselbach.com)
    The script is provided "AS IS" with no warranties.
    .PARAMETER ObjectID
    The Object ID to convert
    .EXAMPLE
    $objectId = "28c3d2c9-5fc7-46d8-8f6c-8e0c1aeb044f"
    Convert-AzureAdObjectIdToSid -ObjectId $objectId
    .EXAMPLE
    Convert-AzureAdObjectIdToSid -AzureADGroup 'Company-Standard-Users'
    .EXAMPLE
    Convert-AzureAdObjectIdToSid -AzureADRole 'Device Administrators'
    Gets the Device Admin role SID
    #>
    [cmdletbinding()]
    param(
        [Parameter(Mandatory, ParameterSetName='ObjectId')]
        [string] $ObjectId,

        [Parameter(Mandatory, ParameterSetName='AzureADGroup')]
        [string] $AADGroupDisplayName,

        [Parameter(Mandatory,ParameterSetName='AzureADRole')]
        [string] $AzureADRole
    )

    switch ($PSCmdlet.ParameterSetName) {
        'AzureADGroup' {
            $ObjectId = (Get-AADGroup -DisplayName $AADGroupDisplayName).ObjectID
        }
        'AzureADRole' {
            $ObjectId = (Get-AzureADDirectoryRole | Where-Object {$_.displayName -eq $AzureADRole}).ObjectID
        }
    }

    $bytes = [Guid]::Parse($ObjectId).ToByteArray()
    $array = New-Object 'UInt32[]' 4

    [Buffer]::BlockCopy($bytes, 0, $array, 0, 16)
    $sid = "S-1-12-1-$array".Replace(' ', '-')

    $sid
}
